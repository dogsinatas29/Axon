use clap::{Parser, Subcommand};

#[derive(Parser)]
#[command(name = "axon")]
#[command(about = "AXON - Agent Orchestration Framework", long_about = None)]
pub struct Cli {
    #[command(subcommand)]
    pub command: Commands,
}

#[derive(Subcommand)]
pub enum Commands {
    /// Initialize a new AXON project
    Init,
    /// Read a blueprint and generate tasks
    Read {
        /// Path to the blueprint file
        path: String,
    },
    /// Run the AXON daemon
    Run,
    /// Check the status of the daemon
    Status,
}
